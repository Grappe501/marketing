function buildCore(f: Row): FeatureCore {
  return {
    ...f,
    whatItDoes: f.whatItDoes ?? [
      `Operator-guided workflows designed to support ${f.shortTitle.toLowerCase()} with explicit checkpoints and traceability.`,
      `Built to be configurable for campaign posture; avoids implying official government systems or guaranteed third-party behavior.`,
    ],
    operatorValue: f.operatorValue ?? [
      "Surfaces owners, status, blockers, and next actions so teams stop rebuilding reality in side threads.",
    ],
    campaignValue: f.campaignValue ?? [
      "Keeps execution aligned to a governed operating picture: fewer surprises, faster coordinated response.",
    ],
    relatedFeatureSlugs: f.relatedFeatureSlugs ?? [],
    pageSections: ["overview", "operator", "leadership", "risk", "systems", "example", "connected", "cta"],
  };
}

function build(f: Row): Feature {
  return finalizeFeature(buildCore(f), FEATURE_DEEP_OVERRIDES[f.slug]);
}

